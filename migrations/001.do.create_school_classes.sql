  startdate Date,
  finishdate Date,
  building text,
  room text,
  teacher text,
  starttime text,
  endtime text,
  sun boolean,
  mon boolean,
  tue boolean,
  wed boolean,
  thurs boolean,
  fri boolean,
  sat boolean
);

